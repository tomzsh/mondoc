const knownMap = new Map(
  KNOWN_SPENDER_LIST.map((s) => [s.address.toLowerCase(), s.label]),
);

export function isKnownSpender(address: Address | string): boolean {
  try {
    return knownMap.has(getAddress(address as Address).toLowerCase());
  } catch {
    return knownMap.has(String(address).toLowerCase());
  }
}

export function getSpenderLabel(address: Address | string): string | undefined {
  try {
    return knownMap.get(getAddress(address as Address).toLowerCase());
  } catch {
    return knownMap.get(String(address).toLowerCase());
  }
}

/** Allow runtime extension (e.g. from API) */
export function registerKnownSpender(address: Address, label: string) {
  knownMap.set(address.toLowerCase(), label);
}

export function knownSpenderCount(): number {
  return knownMap.size;
}

